<section id="featured_image_sizes">
	<div class="page-header"><h3>Recommended Featured Image Sizes</h3><hr class="notop"></div>
<p>
	Ness is meant to show off beautiful photography, so to avoid having ugly pixelated photos, the recommended image size for full-screen background images is 1600px x 900px. However, if your images are smaller that this, it should still all work fine, but may look slightly pixelated on very big desktop screens.</p>


