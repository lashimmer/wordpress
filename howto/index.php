<section id="translation">
	<div class="page-header"><h3>Translation</h3><hr class="notop"></div>
<p>
	Ness comes with everything setup to easily translate it into any language.&nbsp;</p>
<p>
	English and Spanish are already prepared, and many more will be added over time, but if yours isn't translate yet, you can do it yourself very easily. To translate into a different language:</p>

<p>1- Go to the theme translation folder, which is located in "ness/library/translation/"</p>
<p>2- Make a copy of the file 'default.po' and rename the file to the locale of your language, example: Spanish would be 'es_ES.po'</p>
<p>3- Open Poedit (can be downloaded for free from http://www.poedit.net/) and open the file you have created in it</p>
<p>4- Translate all the strings and save your changes. After you save, you will have two files, example: Spanish would have 'es_ES.po' and 'es_ES.mo'</p>
<p>5- Upload these files to "ness/library/translation/"</p>
<p>6- Make sure your WordPress config file (located in the root of your website "yoursite/wp-config.php") has the following line set to your locale: "define ('WPLANG', 'es_ES');"</p>
