    <section id="installing_ness">
    <div class="page-header"><h3>Installing Ness</h3><hr class="notop"></div>
<p>1- After downloading the theme from Themeforest, extract the contents of the downloaded zip file to your computer.</p>
<p>2- In your Wordpress backend, go to <em>Appearance -&gt; Themes -&gt; Add New (button on the top)</em>.</p>
<p>3- Click the upload theme button that is near the top of the screen and go to the folder where you extracted the previous zip file, and select the file called "ness.zip"</p>
<p>4- Click on "Install Now" and once completed select "Activate".</p>
<p>5- You will see a notice about installing a plugin called "Envato WordPress Toolkit", which is a plugin created by Envato to allow you to update Themeforest themes from your site's admin area. It is completely optional, but it makes updating themes slightly easier. For more information about this, scroll to the "Updating Theme" section in this documentation.</p>

</section>



    <section id="important_first_steps">
    <div class="page-header"><h3>Important First Steps</h3><hr class="notop"></div>
<p>
    If you are installing Ness on a website that already has images uploaded, please follow these important steps once Ness is activated:</p>
<p>
    After logging into your admin Wordpress account:</p>
<ol>
    <li>
        Go to <strong><em>Plugins -&gt; Add New&nbsp;</em></strong>and install a free plugin called &quot;<u><em>Force Regenerate Thumbnails</em></u>&quot;</li>
    <li>
        After Installing and activating it, go to <strong><em>Tools -&gt; Force Regenerate Thumbnails&nbsp;</em></strong></li>
    <li>
        Click on &quot;<em><u>Regenerate All Thumbnails</u></em>&quot; and wait until it is finished</li>
</ol>
<p>
    This fixes all your existing images to work with Ness. Any images uploaded after Ness is active will have the appropriate thumbnails created automatically so after doing this process once, you can delete the plugin (in other words, this process needs to be done once whenever you change to any new WordPress theme).</p>
</section>
