<p class="western" lang="en-GB" style="margin-bottom: 0cm">
	After logging into your admin Wordpress account:</p>
<p class="western" lang="en-GB" style="margin-bottom: 0cm">
	1- Go to <strong><em>Appearance &rarr; Theme Options&nbsp;</em></strong><strong><em>&rarr;</em></strong><strong><em>&nbsp;General</em></strong></p>
