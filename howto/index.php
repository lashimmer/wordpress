<p> You can select the blog style you wish to use on the homepage, the pagination style (Infinite scroll, Infinite scroll with load more button or numbered pagination) and enable/disable the full-screen slider at the top of the homepage. The slider has a slider to select the number of posts to show and an option of what posts to show (Latest/by category/by tags)</p>

</section>

