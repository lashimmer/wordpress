<section id="infinite_scroll">
	<div class="page-header"><h3>Infinite Scroll</h3><hr class="notop"></div>
<p>Every category/tag page and the homepage have individual options to have their own pagination style.</p>
<p>
	To enable infinite scroll on the homepage</p>
<p>
	1- Go to&nbsp;<strong><em>Appearance &rarr; Theme Options&nbsp;</em></strong><strong><em>&rarr;</em></strong><strong><em>&nbsp;Homepage</em></strong></p>
<p>
	2- Under the "Pagination Style" option, select an option from the dropdown. It can be either set to "Number Pagination / Infinite Scroll / Infinite Scroll with Load More Button".</p>
<p>
	To enable infinite scroll on a category page</p>
<p>
	1- Go to&nbsp;<strong><em>Posts &rarr; Categories&nbsp;</em></strong></p>
<p>
	2- Add/Edit a category and you will see an "Pagination Style" option, select an option from the dropdown. It can be either set to "Number Pagination / Infinite Scroll / Infinite Scroll with Load More Button" and save the category settings.</p>
	<p>
	To enable infinite scroll on a Tag page</p>
<p>
	1- Go to&nbsp;<strong><em>Posts &rarr; Tags&nbsp;</em></strong></p>
<p>
	2- Add/Edit a Tags and you will see an "Pagination Style" option, select an option from the dropdown. It can be either set to "Number Pagination / Infinite Scroll / Infinite Scroll with Load More Button" and save the tag settings.</p>
<p>
	&nbsp;</p>
<p>
	Couldn't be simpler.</p>

</section>

<section id="typography">
	<div class="page-header"><h3>Typography</h3><hr class="notop"></div>
<p>
	Ness can use any of the several hundreds of Google fonts available. You can select one font for the body text, another font for the general body and headers, and an optional third font that is only used on the post content, this is in case you want your articles text to have extra flavor. The theme demo site uses Raleway, Raleway and for the Post content font, it uses Merriweather.</p>
<p>
	Ness comes with a dropdown option of recommended fonts:</p>
<p>
	1- Go to <strong><em>Appearance -&gt; Theme Options -&gt; Typography </em></strong></p>
<p>
	2- Select a font in the relevant drop downs</p>
<p>
	3- Click on &ldquo;<u><em>Save Changes</em></u>&rdquo;</p>
<p>
	&nbsp;</p>
<p>
	To use a Google Font not in the dropdown lists:</p>
<p>
	1- Go to<em><strong> Appearance -&gt; Theme Options -&gt; Typography </strong></em></p>
<p>
	2- Click the dropdown options and select "Other Google Font", then enter the code provided by Google Fonts (www.google.com/fonts) in Quickuse Step 4 without the &ldquo;Font-family:&rdquo; part. Example if step 4 says <em><u>font-family: &#39;Cantora One&#39;, sans-serif;</u></em> you should only enter <em><u>&#39;Cantora One&#39;, sans-serif;</u></em></p>
<p>
	3- Click on &ldquo;<u><em>Save Changes</em></u>&rdquo;</p>
</section>
